    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
      try {
        double basic = Double.parseDouble(basicSalaryField.getText());
        double allowance = Double.parseDouble(allowanceField.getText());
        double deduction = Double.parseDouble(deductionField.getText());

        double total = basic + allowance - deduction;
        totalSalaryField.setText(String.format("%.2f", total));
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_calculateButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
    String selectedUser = userCombo.getSelectedItem().toString();
    int employeeId = Integer.parseInt(selectedUser.split(" - ")[0]);

    String basicSalary = basicSalaryField.getText();
    String allowance = allowanceField.getText();
    String deduction = deductionField.getText();
    String totalSalary = totalSalaryField.getText();

    // You can add a date picker or use current date
    String salaryDate = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

    if (basicSalary.isEmpty() || allowance.isEmpty() || deduction.isEmpty() || totalSalary.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Fill in all fields", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String sql = "INSERT INTO salary (employee_id, basic_salary, allowance, deduction, total_salary, salary_date) " +
                 "VALUES ('" + employeeId + "', '" + basicSalary + "', '" + allowance + "', '" + deduction + "', '" + totalSalary + "', '" + salaryDate + "')";

    dbConnector db = new dbConnector();
    int result = db.insertData(sql);

    if (result == 1) {
        JOptionPane.showMessageDialog(this, "Salary saved successfully!");
        // Reload salary table or clear fields if needed
    } else {
        JOptionPane.showMessageDialog(this, "Failed to save salary.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_saveButtonActionPerformed

