  private void loadEmployeesToComboBox() {
    userCombo.removeAllItems(); // Clear previous items

    String sql = "SELECT id, fname, surname FROM register";

    try {
        dbConnector db = new dbConnector();
        ResultSet rs = db.getData(sql);

        while (rs.next()) {
            int id = rs.getInt("id");
            String fullName = rs.getString("fname") + " " + rs.getString("surname");
            userCombo.addItem(id + " - " + fullName);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}
  private void loadSalaryTable() {
    DefaultTableModel model = (DefaultTableModel) salaryTable.getModel();
    model.setRowCount(0); // Clear existing rows

    
    if (model.getColumnCount() == 0) {
        model.setColumnIdentifiers(new Object[]{
            "ID", "Employee Name", "Basic Salary", "Allowance", "Deduction", "Total Salary", "Salary Date"
        });
    }

    String sql = "SELECT s.id, r.fname, r.surname, s.basic_salary, s.allowance, s.deduction, s.total_salary, s.salary_date " +
                 "FROM salary s JOIN register r ON s.employee_id = r.id ORDER BY s.salary_date DESC";

    try {
        dbConnector db = new dbConnector();
        ResultSet rs = db.getData(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("fname") + " " + rs.getString("surname"),
                rs.getDouble("basic_salary"),
                rs.getDouble("allowance"),
                rs.getDouble("deduction"),
                rs.getDouble("total_salary"),
                rs.getDate("salary_date")
            });
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading salary records: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

