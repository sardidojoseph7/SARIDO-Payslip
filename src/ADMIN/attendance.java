    private void loadEmployeesToComboBox() {
    userCombo.removeAllItems(); 

    String sql = "SELECT id, fname, surname FROM register";

    try {
        dbConnector db = new dbConnector();
        ResultSet rs = db.getData(sql);

        while (rs.next()) {
            int id = rs.getInt("id");
            String fullName = rs.getString("fname") + " " + rs.getString("surname");
            userCombo.addItem(id + " - " + fullName);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

private void loadAttendanceTable() { 
    String[] headers = {"ID", "Employee Name", "Date", "Time In", "Time Out", "Status"};
    DefaultTableModel model = new DefaultTableModel(headers, 0);
    attendanceTable.setModel(model);

    String sql = "SELECT a.id, r.fname, r.surname, a.date, a.time_in, a.time_out, a.status " +
                 "FROM attendance a JOIN register r ON a.employee_id = r.id";

    try {
        dbConnector db = new dbConnector(); // Use your db helper
        ResultSet rs = db.getData(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("fname") + " " + rs.getString("surname"),
                rs.getString("date"),
                rs.getString("time_in"),
                rs.getString("time_out"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}



