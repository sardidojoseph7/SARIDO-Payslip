    private void RecordAttendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RecordAttendanceActionPerformed
           String selectedUser = userCombo.getSelectedItem().toString();
    int employeeId = Integer.parseInt(selectedUser.split(" - ")[0]);
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String date = sdf.format(attendancedate.getDate());
   String timeInInput = Timein.getText().trim();
String timeOutInput = Timeout.getText().trim();

SimpleDateFormat inputFormat = new SimpleDateFormat("h:mm a"); // e.g. 7:00 AM
SimpleDateFormat outputFormat = new SimpleDateFormat("HH:mm:ss"); // 24-hour format for MySQL TIME

String timeIn = "";
String timeOut = "";

try {
    Date parsedTimeIn = inputFormat.parse(timeInInput);
    timeIn = outputFormat.format(parsedTimeIn);

    Date parsedTimeOut = inputFormat.parse(timeOutInput);
    timeOut = outputFormat.format(parsedTimeOut);
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Invalid time format. Use like 7:00 AM", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}
    String status = Status.getSelectedItem().toString();

    if (date.isEmpty() || timeIn.isEmpty() || timeOut.isEmpty() || status.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String sql = "INSERT INTO attendance (employee_id, date, time_in, time_out, status) " +
                 "VALUES ('" + employeeId + "', '" + date + "', '" + timeIn + "', '" + timeOut + "', '" + status + "')";

    dbConnector db = new dbConnector();
    int result = db.insertData(sql);

    if (result == 1) {
        JOptionPane.showMessageDialog(this, "Attendance recorded successfully!");
        loadAttendanceTable(); // Refresh table if needed
    } else {
        JOptionPane.showMessageDialog(this, "Failed to record attendance.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_RecordAttendanceActionPerformed

